/*
 * Extract one field from a full marc record.
 */
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int
mr_field(marc_rec, tag, field)
char    *marc_rec;
char    *tag;
char    *field;
{
    char    *dirptr;
    char    *dataptr;
    int     recbase;
    char    field_tag[4];
    int     field_len;
    int     field_off;

    sscanf(marc_rec, "%*12c%5d", &recbase);
    dirptr  = marc_rec + 24;
    dataptr = marc_rec + recbase;
    while (*dirptr != '\036') {
	memset(field_tag, '\0', sizeof(field_tag));
	sscanf(dirptr,"%3c%4d%5d",  field_tag, &field_len, &field_off);
        if (strncmp(field_tag, tag, 3) == 0) {
      	    strncpy(field, dataptr+field_off, field_len);
    	    field[field_len] = '\0';
	    /* Keep the field terminator. */
	    /*
	    field[field_len-1] = '\0';
	    */
	}
	dirptr += 12;
    }
    return 1;
}
