#ifndef DBUF_H
#define DBUF_H

/*      Dynamic string structure definition.
 */
typedef struct dbuf {
	 int     size;    /* current size of the data.    */
	 int     max;     /* the amount of space allocated to the data    */
	 int	 cursor;  /* Where to read the next octet of data.	*/
	 unsigned char    *data;
} DBUF;

#ifndef __DMALLOC_H__
#ifndef DMALLOC_INCLUDE
#define	MALLOC(x) (x *)calloc(1, sizeof(x))
#define	CALLOC(x, y) (x *)calloc(y, sizeof(x))
#endif
#endif
DBUF *NewDbuf();

#endif	/* DBUF_H	*/

